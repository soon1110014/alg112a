import random, copy

def neighbor(f, p, h=0.01):
    """產生鄰近的解及其高度"""
    p1= p.copy()
    for i in range(len(p)):
        p1[i] = p1[i] +random.uniform(-h,h)
    f1 = f(p1)
    return p1, f1

def hillClimbing(f, p, h=0.01):
    failCount = 0                    
    while (failCount < 10000):       
        fnow = f(p)                  
        p1, f1 = neighbor(f, p, h)
        if f1 >= fnow:              
            fnow = f1                
            p = p1
            print('p=', p, 'f(p)=', fnow)
            failCount = 0            
        else:                        
            failCount = failCount + 1
    return (p,fnow)                 

